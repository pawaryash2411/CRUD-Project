const mongoose = require("mongoose");

const RegisterSchema = mongoose.Schema({
  name: {
    type: String,
    required: true,
    unique: true,
  },
  email: {
    type: String,
    required: true,
  },
  password: {
    type: String,
    required: true,
  },
});

const RegisterModel = mongoose.model("register", RegisterSchema);

module.exports = RegisterModel;
